    private void btmTestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btmTestActionPerformed
        try {
            Connection bd = ConexionSQL.getBD();
            if (txtIp.getText().isEmpty() || txtPuerto.getText().isEmpty()) {
                throw new PersonalizedExc(PropertiesCache.Idiomas.getString("DatosVacios"));
            }
            if (!ConexionSQL.Conectar(txtIp.getText(), txtPuerto.getText(), "VideoJuegos", "escritorio", "escritorio")) {
                throw new PersonalizedExc(PropertiesCache.Idiomas.getString("TestErroneo"));
            }
            JOptionPane.showMessageDialog(null, PropertiesCache.Idiomas.getString("TestCorrecto"));
            localConnection = ConexionSQL.getBD();
            ConexionSQL.setCon(bd);
        } catch (PersonalizedExc per) {
            per.GetError(per.getMessage());
        }
    }//GEN-LAST:event_btmTestActionPerformed

    private void btmConnectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btmConnectActionPerformed
        try {
            if (!ConexionSQL.IsConected() || localConnection==null) {
                throw new PersonalizedExc(PropertiesCache.Idiomas.getString("RealizarTest"));
            }
            PropertiesCache cache = PropertiesCache.getInstance();
            cache.setProperty("ip", txtIp.getText());
            cache.setProperty("puerto", txtPuerto.getText());
            cache.flush();
            ConexionSQL.setCon(localConnection);
            dispose();
        } catch (PersonalizedExc per) {
            per.GetError(per.getMessage());
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btmConnectActionPerformed

